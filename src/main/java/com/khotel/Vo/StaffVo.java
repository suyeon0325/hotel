package com.khotel.Vo;

public class StaffVo {
	private String StaffId = null;
	private String StaffName = null;
	private String StaffBirth = null;
	private String StaffEmail = null;
	private String StaffSex = null;
	private String StaffAddress = null;
	private String StaffPhone = null;
	private String Floor = null;
	
	
	public String getStaffId() {
		return StaffId;
	}
	public void setStaffId(String staffId) {
		StaffId = staffId;
	}
	public String getStaffName() {
		return StaffName;
	}
	public void setStaffName(String staffName) {
		StaffName = staffName;
	}
	public String getStaffBirth() {
		return StaffBirth;
	}
	public void setStaffBirth(String staffBirth) {
		StaffBirth = staffBirth;
	}
	public String getStaffEmail() {
		return StaffEmail;
	}
	public void setStaffEmail(String staffEmail) {
		StaffEmail = staffEmail;
	}
	public String getStaffSex() {
		return StaffSex;
	}
	public void setStaffSex(String staffSex) {
		StaffSex = staffSex;
	}
	public String getStaffAddress() {
		return StaffAddress;
	}
	public void setStaffAddress(String staffAddress) {
		StaffAddress = staffAddress;
	}
	public String getStaffPhone() {
		return StaffPhone;
	}
	public void setStaffPhone(String staffPhone) {
		StaffPhone = staffPhone;
	}
	public String getFloor() {
		return Floor;
	}
	public void setFloor(String floor) {
		Floor = floor;
	}
	
}
